public class arithmaticoperator {
    public static void main(String[] args) {
        int x=5, y=3 ;

        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x%y);
        System.out.println(x/y);
        System.out.println(x+y+x);

        x++ ;
        y++ ;
        System.out.println(x+y);
    }
}
