public class assignmentoperator {
    public static void main(String[] args) {
        int x=5;
        int y;

        // = operator
      y=x ;
        System.out.println(y);
        // +=
        x+=y ;
        System.out.println(x);
        // x-=
        y-=x;
        System.out.println(y);
        // /=
        x/=y;
        System.out.println(x); //-2
    }
}
